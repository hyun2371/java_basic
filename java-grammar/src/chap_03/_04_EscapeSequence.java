package chap_03;

public class _04_EscapeSequence {
    public static void main(String[] args) {
        // 특수문자, 이스케이프 문자 (Escape Sequence, Escape Character, Special Character

        // \n 줄바꿈
        System.out.println("샌드위치가 \n너무 \n맛있어요");


        // \t 탭
        System.out.println("등심까스\t\t9000원");
        System.out.println("크래페\t\t5000원");

        // \\ 역슬래시
        System.out.println("gahyun\\JavaStudy\\java_basic");

        // \"
        System.out.println("삼색이가 \"냐옹\"했다.");
        System.out.println('\'');
        System.out.println("삼색이가 '뭘 봐?' 라는 표정을 지었다");
        //System.out.println('삼색이가 "뭘 봐?" 라는 표정을 지었다');
    }
}
